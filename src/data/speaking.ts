/**
 * Helper function to sort speaking engagements
 */
export function getSortedEngagements() {
    return [...speakingEngagements].sort((a, b) => {
        // First sort by status (upcoming before past)
        if (a.status !== b.status) {
            return a.status === 'upcoming' ? -1 : 1;
        }
        
        // For same status, sort by date
        // Parse dates like "June 13, 2024" or any other standard format
        const dateA = new Date(a.date);
        const dateB = new Date(b.date);
        
        // Upcoming: ascending (nearest first)
        // Past: descending (most recent first)
        return a.status === 'upcoming' 
            ? dateA.getTime() - dateB.getTime() 
            : dateB.getTime() - dateA.getTime();
    });
}

/**
 * Get all unique tags from speaking engagements
 */
export function getAllSpeakingTags() {
    return [...new Set(
        speakingEngagements
            .flatMap(engagement => engagement.tags || [])
            .sort()
    )];
}

